/**
 * Create a program that prints the integers (whole numbers) from 100 to 1.
 * The program output should be:
 *
 * 100
 * 99
 * 98
 * (many rows of numbers here)
 * 3
 * 2
 * 1
 */

public class Uzd28 {
    public static void main(String[] args) {

        int i = 100;

        while (i >= 1) {
            System.out.println(i);
            i--;
        }
    }
}